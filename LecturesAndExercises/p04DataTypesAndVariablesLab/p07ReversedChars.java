package LecturesAndExercises.p04DataTypesAndVariablesLab;

import java.util.Scanner;

public class p07ReversedChars {
    public static void main(String[] args) {

        Scanner scanner = new Scanner(System.in);
        String char1 = scanner.nextLine();
        String char2 = scanner.nextLine();
        String char3 = scanner.nextLine();
        System.out.printf("%s %s %s", char3, char2, char1);
    }
}
